# --- THE GPT-4o EDITOR LOGIC WITH CHUNK TRACKING ---
def refine_transcript_with_gpt(raw_text: str) -> str:
    """Fixes speaker tags across chunks with explicit context."""
    if not raw_text or not raw_text.strip():
        return raw_text

    api_key = (os.getenv("AZURE_OPENAI_KEY") or os.getenv("OPENAI_API_KEY") or "").strip()
    azure_endpoint = os.getenv("AZURE_OPENAI_ENDPOINT", "").strip()
    api_version = os.getenv("AZURE_OPENAI_API_VERSION", "2025-04-01-preview").strip()
    deployment = os.getenv("AZURE_DIARIZE_DEPLOYMENT", "gpt-4o").strip()

    try:
        if azure_endpoint:
            llm_client = AzureOpenAI(api_key=api_key, api_version=api_version, azure_endpoint=azure_endpoint)
            model_name = deployment
        else:
            llm_client = OpenAI(api_key=api_key)
            model_name = "gpt-4o"

        system_prompt = """You are an expert transcript editor for professional meetings. 
        The transcript below was generated by an AI that processed the audio in smaller chunks. 
        Because of this, the speaker tags (e.g., [Speaker 1], [Speaker 2]) RESET at every '--- CHUNK X ---' marker.
        For example, 'Speaker 1' in CHUNK 1 might be a completely different person than 'Speaker 1' in CHUNK 2.
        
        Your task:
        1. Read the flow of the conversation carefully.
        2. Identify the logical distinct speakers (e.g., Interviewer, Candidate, Boy, Girl, Client, etc.).
        3. Replace all the reset tags with consistent GLOBAL speaker names across the entire transcript (e.g., [Person A], [Person B], [Person C]).
        4. Remove the '--- CHUNK X ---' markers from your final output.
        5. DO NOT summarize or alter the actual spoken text. Output ONLY the fully corrected transcript."""

        response = llm_client.chat.completions.create(
            model=model_name,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": f"Here is the raw transcript:\n\n{raw_text}"}
            ],
            temperature=0.1
        )
        return response.choices[0].message.content.strip()
    except Exception as e:
        print(f"GPT Refinement failed: {e}")
        return raw_text


