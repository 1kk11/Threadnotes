@app.post("/transcribe")
def process_meeting(file: UploadFile = File(...), meeting_type: str = Form("General")):
    tmp_path = None
    filename = file.filename
    progress_tracker[filename] = {"current": 0, "total": 1}

    try:
        file_ext = pathlib.Path(filename.lower()).suffix
        audio_exts = {'.mp3', '.wav', '.webm', '.m4a', '.mpeg', '.mp4'}
        doc_exts = {'.docx', '.txt'}

        if file_ext in audio_exts:
            with tempfile.NamedTemporaryFile(delete=False, suffix=file_ext) as tmp:
                shutil.copyfileobj(file.file, tmp)
                tmp_path = tmp.name

            chunk_dir = tempfile.mkdtemp()
            chunk_files = []
            
            # --- HIGH QUALITY VAD CHUNKING (Prevents Audio Flattening) ---
            try:
                from pydub import AudioSegment
                from pydub.silence import split_on_silence
                
                audio = AudioSegment.from_file(tmp_path)
                
                raw_chunks = split_on_silence(
                    audio,
                    min_silence_len=1000,
                    silence_thresh=audio.dBFS - 14,
                    keep_silence=500
                )
                if not raw_chunks:
                    raw_chunks = [audio]

                target_length = 45 * 1000 
                grouped_chunks = []
                current_chunk = AudioSegment.empty()
                
                for c in raw_chunks:
                    current_chunk += c
                    if len(current_chunk) >= target_length:
                        grouped_chunks.append(current_chunk)
                        current_chunk = AudioSegment.empty()
                if len(current_chunk) > 0:
                    grouped_chunks.append(current_chunk)

                for i, gc in enumerate(grouped_chunks):
                    c_path = os.path.join(chunk_dir, f"chunk_{i:04d}.wav")
                    gc.export(c_path, format="wav") # Preserves high quality for Diarization
                    chunk_files.append(c_path)
                    
            except Exception as e:
                print(f"VAD Chunking failed: {e}")
                raise HTTPException(status_code=500, detail="Audio chunking failed.")

            ts = Transcriber()
            transcript_parts = {}
            active_futures = {}
            
            progress_tracker[filename]["total"] = max(1, len(chunk_files))

            def process_single_chunk(idx, chunk_path):
                max_retries = 2
                backoff_time = 2
                for attempt in range(max_retries):
                    try:
                        text = ts.diarize_chunk_file(chunk_path)
                        if os.path.exists(chunk_path):
                            os.remove(chunk_path)
                        return idx, text
                    except Exception:
                        if attempt < max_retries - 1:
                            time.sleep(backoff_time)
                            backoff_time *= 2
                        else:
                            if os.path.exists(chunk_path):
                                os.remove(chunk_path)
                            return idx, ""

            with ThreadPoolExecutor(max_workers=MAX_WORKERS) as executor:
                for idx, chunk_path in enumerate(chunk_files):
                    future = executor.submit(process_single_chunk, idx, chunk_path)
                    active_futures[future] = idx

                for future in concurrent.futures.as_completed(active_futures.keys(), timeout=300):
                    try:
                        idx, text = future.result(timeout=60)
                        if text:
                            transcript_parts[idx] = text
                    except concurrent.futures.TimeoutError:
                        print("Timeout error in a chunk")
                    except Exception as e:
                        print(f"Chunk processing error: {e}")
                    
                    progress_tracker[filename]["current"] += 1

            # --- ADDING CHUNK MARKERS FOR GPT CONTEXT ---
            raw_final_text = ""
            for i in sorted(transcript_parts.keys()):
                if transcript_parts[i].strip():
                    raw_final_text += f"\n\n--- CHUNK {i+1} ---\n{transcript_parts[i].strip()}"
            
            # --- APPLY GPT-4o CORRECTION ON THE RAW TEXT ---
            final_text = refine_transcript_with_gpt(raw_final_text)

            progress_tracker.pop(filename, None)

            if tmp_path and os.path.exists(tmp_path):
                os.remove(tmp_path)
            if os.path.exists(chunk_dir):
                shutil.rmtree(chunk_dir, ignore_errors=True)

            return {"status": "success", "meeting_type": meeting_type, "transcript": final_text}

        elif file_ext in doc_exts:
            content = file.file.read()
            if file_ext == '.docx':
                import io
                from docx import Document
                doc = Document(io.BytesIO(content))
                text = " ".join([p.text for p in doc.paragraphs if p.text.strip()])
            else:
                text = content.decode('utf-8', errors='ignore')
            return {"status": "success", "meeting_type": meeting_type, "transcript": text}
        return {"status": "error", "message": "Unsupported format"}

    except Exception as e:
        progress_tracker.pop(filename, None)
        if tmp_path and os.path.exists(tmp_path):
            os.remove(tmp_path)
        return {"status": "error", "message": str(e)}

